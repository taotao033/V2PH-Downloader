def normalize(lang: str | None) -> str:
    return lang if lang in SUPPORTED else DEFAULT_LANG


def translate(lang: str, key: str) -> str:
    lang = normalize(lang)
    table = TRANSLATIONS.get(lang, {})
    if key in table:
        return table[key]
    en = TRANSLATIONS.get("en", {})
    return en.get(key, key)
